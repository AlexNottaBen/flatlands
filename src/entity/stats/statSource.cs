namespace Statistycs{

public enum StatOrigin{

    /// <summary>
    /// any of basic entity stats
    /// </summary>
    entity,

    /// <summary>
    /// basic stats of item
    /// </summary>
    item,

    /// <summary>
    /// reforges for weapons, armor or other items
    /// </summary>
    forge,

    /// <summary>
    /// runes applied to the items
    /// </summary>
    rune,

    /// <summary>
    /// any possible modifiers got from player's skill tree
    /// </summary>
    skill,

}



}